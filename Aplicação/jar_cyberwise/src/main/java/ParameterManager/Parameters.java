package ParameterManager;

public class Parameters {

    private Double alertaCPU;
    private Double criticoCPU;
    private Double alertaDISCO;
    private Double criticoDISCO;
    private Double alertaRAM;
    private Double criticoRAM;

    public Double getAlertaCPU() {
        return alertaCPU;
    }

    public void setAlertaCPU(Double alertaCPU) {
        this.alertaCPU = alertaCPU;
    }

    public Double getCriticoCPU() {
        return criticoCPU;
    }

    public void setCriticoCPU(Double criticoCPU) {
        this.criticoCPU = criticoCPU;
    }

    public Double getAlertaDISCO() {
        return alertaDISCO;
    }

    public void setAlertaDISCO(Double alertaDISCO) {
        this.alertaDISCO = alertaDISCO;
    }

    public Double getCriticoDISCO() {
        return criticoDISCO;
    }

    public void setCriticoDISCO(Double criticoDISCO) {
        this.criticoDISCO = criticoDISCO;
    }

    public Double getAlertaRAM() {
        return alertaRAM;
    }

    public void setAlertaRAM(Double alertaRAM) {
        this.alertaRAM = alertaRAM;
    }

    public Double getCriticoRAM() {
        return criticoRAM;
    }

    public void setCriticoRAM(Double criticoRAM) {
        this.criticoRAM = criticoRAM;
    }
}
